package ch1;

public class Methods {

	String name;

	// this is a method. It has a string return type.
	// the method name is getName. It has no parameters.
	// it returns the name in the field above
	// it is known as a setter
	// it has a public access modifier
	public String getName() {
		return name;
	}

	// this is simiar to above, but sets the field to
	// the name passed in the parameter
	// the void return type means do not return a value.
	// It means there is no obligation to return the value
	public void setName(String newName) {
		name = newName;
	}
	
	// the full declaration of a method is called a method signature
	
	
	
	
}
